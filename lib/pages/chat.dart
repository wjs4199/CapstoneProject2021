  /// 사용자의 핸드폰 내에 있는 갤러리의 사진을 가져오는 future
  Future getImage() async {
    var imagePicker = ImagePicker();
    PickedFile pickedFile;

    pickedFile = await imagePicker.getImage(source: ImageSource.gallery);
    if (pickedFile != null) {
      imageFile = File(pickedFile.path);
      if (imageFile != null) {
        setState(() {
          isLoading = true;
        });
        await uploadFile();
      }
    }
  }

  void getSticker() {
    // Hide keyboard when sticker appear
    focusNode.unfocus();
    setState(() {
      isShowSticker = !isShowSticker;
    });
  }
  /// getImage 에서 파일 업로드 하는 future
  Future uploadFile() async {
    var fileName = DateTime.now().millisecondsSinceEpoch.toString();
    var reference = FirebaseStorage.instance.ref().child(fileName);
    var uploadTask = reference.putFile(imageFile);

    try {
      var snapshot = await uploadTask;
      imageUrl = await snapshot.ref.getDownloadURL();
      setState(() {
        isLoading = false;
        onSendMessage(imageUrl, 1);
      });
    } on FirebaseException catch (e) {
      setState(() {
        isLoading = false;
      });
      await Fluttertoast.showToast(msg: e.message ?? e.toString());
    }
  }


  ///message 보내는 부분
  void onSendMessage(String content, int type) async {
    /// 보내는 메시지 type: 0 = text, 1 = image(이모티콘, local에 있는 gif_추후에 추가), 2 = sticker
    if (content.trim() != '') {
      textEditingController.clear();
      var documentReference_chatRoom = FirebaseFirestore.instance
          .collection('chatRoom')
          .doc(groupChatId);

      await  FirebaseFirestore.instance.runTransaction((transaction) async {
        transaction.set(
          documentReference_chatRoom,
          {
            'timestamp': FieldValue.serverTimestamp(),
            'idFrom': id,
            'idTo': peerId,
            'peerAvatar': peerAvatar,
            'peerNickname' : peerName,
            'myNickname' : myName,
            'myAvatar' : myAvatar,
            'isRead' : 'new',
            'isShowedNotification': false,
          },
        );
      });



      var documentReference = FirebaseFirestore.instance
          .collection('messages')
          .doc(groupChatId)
          .collection(groupChatId)
          .doc(DateTime.now().millisecondsSinceEpoch.toString());

      await FirebaseFirestore.instance.runTransaction((transaction) async {
        transaction.set(
          documentReference,
          {
            'idFrom': id,
            'idTo': peerId,
            'timestamp': DateTime.now().millisecondsSinceEpoch.toString(),
            'content': content,
            'type': type
          },
        );
      });

      await listScrollController.animateTo(0.0,
          duration: Duration(milliseconds: 300), curve: Curves.easeOut);
    } else {
      await Fluttertoast.showToast(
          msg: 'Nothing to send',
          backgroundColor: Colors.black,
          textColor: Colors.red);
    }
  }
